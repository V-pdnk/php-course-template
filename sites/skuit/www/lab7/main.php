<?php
/*1. Проверка чётности числа*/
$randNum = rand(1,100);
/*2. Определение наибольшего из двух чисел*/
$randNum1 = rand(1,100);
$randNum2 = rand(1,100);
$bigNum = null;
if($randNum1 == $randNum2):
    $bigNum = $randNum1;
endif;
$bigNum = ($randNum1 > $randNum2) ? $randNum1:$randNum2;
/*3. Сумма чисел от 1 до N*/
$randNumN = rand(10,100);
$sum = 0;
for($i = 0; $i < $randNumN+1; $i++):
    $sum += $i;
endfor;
/*4. Подсчёт положительных, отрицательных и нулевых чисел*//*5. Поиск минимального числа в массиве*/
for($i = 0; $i <10; $i++):
    $arr[] = rand(-10, 10);
    //echo $arr[$i].' / ';
endfor;
$zeros = 0;
$neg = 0;
$pos = 0;
foreach($arr as $n):
    if($n == 0):
        $zeros ++;
        continue;
    endif;
    $n > 0 ? $pos++:$neg++;
endforeach;

?>

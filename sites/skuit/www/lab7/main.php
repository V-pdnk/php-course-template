    </div>

    <div class="4">
        <h1>4. Подсчёт положительных, отрицательных и нулевых чисел</h1>
        <h2>В массиве <?= $zeros ?> нулей</h2>
        <h2>В массиве <?= $pos ?> положительных чисел</h2>
        <h2>В массиве <?= $neg ?> отрицательных чисел</h2>
    </div>

    <div class="5">
